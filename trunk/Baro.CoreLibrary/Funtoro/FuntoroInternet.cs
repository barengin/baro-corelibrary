        private static RasEntry ExistingEntry(string name)
        {
            var existing = Ras.Entries.FirstOrDefault(e => string.Compare(e.Name, name, true) == 0);
            return existing;
        }

        private static RasError DialEntry(string name)
        {
            var entry = ExistingEntry(name);
            return entry.Dial(false);
        }

        private static void CreateEntry(string name)
        {
            var entry = ExistingEntry(name);

            if (entry != null)
            {
                Ras.DeleteEntry(entry);
            }

            entry = new RasEntry()
            {
                Name = name,
                PhoneNumber = "*99#",
                // TODO: I hard-coded it for FUNTORO AIO
                DeviceName = "COM1:",
                DeviceType = "modem"
            };

            var @params = new RasDialParams()
            {
                UserName = "",
                Password = "",
                Domain = ""
            };

            entry.Options |= (int)(ConnectionOptions.DialAsLocalCall | ConnectionOptions.UseLogonCredentials);

            Ras.CreateEntry(entry, @params);
        }

